module test_kusuryak_九九母數變宮陽圖

using Test

# forward 順     순
# reverse 逆     역
# Δ       增減   증감
#         縱     종    세로
#         橫     횡    가로
# row     行     행
# column  列     렬
#         順序雙 순서쌍
#         方陣   방진

母數雙 = [(2,1) (3,3) (1,2)
          (1,3) (2,2) (3,1)
          (3,2) (1,1) (2,3)]

增減雙 = [(3,0) (6,6) (0,3)
          (0,6) (3,3) (6,0)
          (6,3) (0,0) (3,6)]

方陣 = [[母數 .+ 增減 for 母數 in 母數雙] for 增減 in 增減雙]
@test 方陣[1,1] == [(5, 1)  (6, 3)  (4, 2)
                    (4, 3)  (5, 2)  (6, 1)
                    (6, 2)  (4, 1)  (5, 3)]
@test sum.(方陣[1,1]) == [6  9  6
                          7  7  7
                          8  5  8]
@test sum(sum.(方陣[1,1])) == 63 # 6+3 == 9

using Millboard # table
@test table(方陣) |> string == """
|   |                    1 |                    2 |                    3 |
|---|----------------------|----------------------|----------------------|
|   | (5, 1) (6, 3) (4, 2) | (8, 7) (9, 9) (7, 8) | (2, 4) (3, 6) (1, 5) |
|   | (4, 3) (5, 2) (6, 1) | (7, 9) (8, 8) (9, 7) | (1, 6) (2, 5) (3, 4) |
| 1 | (6, 2) (4, 1) (5, 3) | (9, 8) (7, 7) (8, 9) | (3, 5) (1, 4) (2, 6) |
|   | (2, 7) (3, 9) (1, 8) | (5, 4) (6, 6) (4, 5) | (8, 1) (9, 3) (7, 2) |
|   | (1, 9) (2, 8) (3, 7) | (4, 6) (5, 5) (6, 4) | (7, 3) (8, 2) (9, 1) |
| 2 | (3, 8) (1, 7) (2, 9) | (6, 5) (4, 4) (5, 6) | (9, 2) (7, 1) (8, 3) |
|   | (8, 4) (9, 6) (7, 5) | (2, 1) (3, 3) (1, 2) | (5, 7) (6, 9) (4, 8) |
|   | (7, 6) (8, 5) (9, 4) | (1, 3) (2, 2) (3, 1) | (4, 9) (5, 8) (6, 7) |
| 3 | (9, 5) (7, 4) (8, 6) | (3, 2) (1, 1) (2, 3) | (6, 8) (4, 7) (5, 9) |"""

end # module test_kusuryak_九九母數變宮陽圖
